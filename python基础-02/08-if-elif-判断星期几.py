number = int(input("请输入1-7之间的一个数字："))

if number == 1:
    print("星期一")

elif number == 2:
    print("星期二")
elif number == 3:
    print("星期三")
elif number == 4:
    print("星期四")
elif number == 5:
    print("星期五")
elif number == 6:
    print("星期六")
elif number == 7:
    print("星期天")
else:
    print("请输入1-7之间的数字...")
